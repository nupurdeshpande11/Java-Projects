    public void fillTexts(){
          int index =business.getMotherDirectory().getMotherIndex(request.getMotherNmae()); 
           lblNam.setText(business.getMotherDirectory().getMotherList().get(index).getName());
           lblCourseName.setText(business.getMotherDirectory().getMotherList().get(index).getCourseName());
           lblSalary.setText(String.valueOf((business.getMotherDirectory().getMotherList().get(index).getDreamJobSal())));
           lblStatus.setText(business.getMotherDirectory().getMotherList().get(index).getCourseStatus());
    }
    
    
    public void findJobs(){
         int index =business.getMotherDirectory().getMotherIndex(request.getMotherNmae()); 
        for(Job job : jobFinderOrganization.getJobDirectory().getJobList()){
            System.out.println(job.getJobName());
            if(business.getMotherDirectory().getMotherList().get(index).getCourseName().equalsIgnoreCase(job.getCourseRequired())  )  {
                 DefaultTableModel model = (DefaultTableModel) tblJob.getModel();

        model.setRowCount(0);
            {
            Object[] row = new Object[2];
            row[0] = job;
            row[1] = job.getJobSalary();
               model.addRow(row);
        }
            }
        }
    }
    
    
    
