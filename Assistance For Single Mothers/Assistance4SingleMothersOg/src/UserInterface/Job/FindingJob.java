    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Go to applying job panel
           int selectedRow = tblJob.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select row!");
            return;
        }

        Job job = (Job) tblJob.getValueAt(selectedRow, 0);
         ApplyingJobPanel ApplyingJobPanel = new ApplyingJobPanel(userProcessContainer, account, jobFinderOrganization, enterprise, business,job,request );
        userProcessContainer.add("ApplyingJobPanel", ApplyingJobPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton2ActionPerformed

