    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        String message = messageJTextField.getText();
        String name = txtMother.getText();
        int age = Integer.parseInt(txtAge.getText());

        String babyName = txtBabyName.getText();
        VaccinationDoctorWorkRequest request = new VaccinationDoctorWorkRequest();

        request.setMessage(message);
        request.setmName(name);
        request.setAge(age);
        request.setBabyName(babyName);
        request.setSender(account);
        request.setStatus("Sent");

        Organization org = null;

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof VaccinationDoctorOrganization) {
                org = organization;
                break;
            }
        }

        if (org != null) {
            org.getVaccinationTrackerWorkQueue().getWorkRequestListV().add(request);
            account.getVaccinationTrackerWorkQueue().getWorkRequestListV().add(request);
        }
        populateRequestTable4();
    }//GEN-LAST:event_requestTestJButtonActionPerformed

