    public void populateRequestTable4() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);

        for (VaccinationTrackerWorkRequest request2 : account.getVaccinationTrackerWorkQueue().getWorkRequestListV()) {

            if (request2 instanceof VaccinationDoctorWorkRequest) {

                Object[] row = new Object[7];

                row[0] = request2;
                row[1] = request2.getmName();
                row[2] = request2.getBabyName();

                row[3] = request2.getSender();
                row[4] = request2.getReceiver();
                row[5] = request2.getStatus();
                String result1 = ((VaccinationDoctorWorkRequest) request2).getVaccineName();
                row[6] = result1 == null ? "Waiting" : result1;
                model.addRow(row);
            }

        }
    }

